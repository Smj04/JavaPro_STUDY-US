package Page;

public class User {
	
	private String name;
	private String id;
	private String pass;
	private String regular_chk;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getRegular_chk() {
		return regular_chk;
	}
	public void setRegular_chk(String regular_chk) {
		this.regular_chk = regular_chk;
	}
	
	

}
